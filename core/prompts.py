"""JARVIS system + PC-control prompts.

Phase 3 of the modularisation refactor (2026-05-29). Both BASE_SYSTEM_-
PROMPT (~300 lines) and PC_CONTROL_PROMPT (~1160 lines) used to live as
module-level string literals in bobert_companion.py. Moving them here
trims ~1,450 lines off that file — by far the biggest single
context-reduction win of the entire refactor.

Phase 4 (2026-05-30): the 20-line MCU signature-phrase pool and the
per-USER_TONE behaviour bullets used to live inline inside the
BASE_SYSTEM_PROMPT body. They were hoisted into core/persona.py so
other modules can introspect them programmatically, and the renderers
are spliced into the prompt below at the same call sites. This keeps a
single source of truth for the phrase pool — duplicating the list
would let the LLM see conflicting rotations.

The prompts are otherwise pure string literals (Python implicit string
concatenation inside parens — no f-strings, no runtime computation).
Nothing here imports back into bobert_companion, so there's no
circular-import risk. The parent module does
`from core.prompts import BASE_SYSTEM_PROMPT, PC_CONTROL_PROMPT` and
existing references like `_system_prompt = BASE_SYSTEM_PROMPT` resolve
unchanged.

When editing the prompts in future: keep the implicit-concat style
(adjacent string literals separated only by whitespace inside the
parens). That keeps the diff-per-line surface area small enough for
the pipeline reviewer to skim without context-budget blowout.
"""

